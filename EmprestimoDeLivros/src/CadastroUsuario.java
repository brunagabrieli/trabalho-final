    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        // TODO add your handling code here:
        if ((!jtfCod.getText().equals("")) && (!jtfNome.getText().equals("")) && (!jtfEndereco.getText().equals("")) && (!jtfTelefone.getText().equals(""))){

        int cod = Integer.parseInt(jtfCod.getText());
        String nome = jtfNome.getText();
        String endereco = jtfEndereco.getText();
        String telefone = jtfTelefone.getText();
        
            Usuario usuario = new Usuario ();
            usuario.setCodigo(cod);
            usuario.setNome(nome);
            usuario.setEndereco(endereco);
            usuario.setTelefone(telefone);
            DAO dao = new DAO ();
            if (dao.incluirUsuario(usuario)) {
                JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!!");
                listarUsuario();
            }else{
                JOptionPane.showMessageDialog(null, "O usuário não pode ser cadastrado!!");
                this.dispose();
                
             }
        
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos devem estar preenchidos!!");
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        if ((!jtfCod.getText().equals("")) && (!jtfNome.getText().equals("")) && (!jtfEndereco.getText().equals("")) && (!jtfTelefone.getText().equals(""))){
            
        int cod = Integer.parseInt(jtfCod.getText());
        String nome = jtfNome.getText();
        String endereco = jtfEndereco.getText();
        String telefone = jtfTelefone.getText();
        
            Usuario usuario = new Usuario ();
            usuario.setCodigo(cod);
            usuario.setNome(nome);
            usuario.setEndereco(endereco);
            usuario.setTelefone(telefone);
            DAO dao = new DAO ();
            if (dao.alterarUsuario(usuario, cod)){
            JOptionPane.showMessageDialog(null, "Cadastro Alterado com Sucesso!!");
            listarUsuario();
            }else{
                JOptionPane.showMessageDialog(null, "Os dados não puderam ser alterados!!");
                this.dispose();
            }
        }else{ 
            JOptionPane.showMessageDialog(null, "Todos os campos devem estar preenchidos!!");
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

