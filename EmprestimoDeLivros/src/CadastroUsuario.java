        public void listarUsuario () {
            DAO dao = new DAO ();
            ResultSet rs = dao.consultarNomesUsuario();
            DefaultTableModel tabela = (DefaultTableModel) jtListar.getModel();
            
            while (tabela.getRowCount()>0) {
            tabela.removeRow(0);
        }
        
        Object dados[] = new Object[4];
        try {
            while (rs.next()){
                for(int i = 0; i<4; i++){
                    dados[i] = rs.getString(i+1);
                }
                tabela.addRow(dados);
            }
            dao.encerraConexao();
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }

        }
        
    
